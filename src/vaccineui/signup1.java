public boolean isDateOfBirthValid(JDateChooser dateChooser) {
    // Get the selected date (Date of Birth) from the JDateChooser
    Date dob = dateChooser.getDate();
    
    // Check if no date is selected
    if (dob == null) {
        JOptionPane.showMessageDialog(null, "Please select a date of birth.");
        return false; 
    }

    // Get today's date
    Calendar today = Calendar.getInstance();
    today.set(Calendar.HOUR_OF_DAY, 0);
    today.set(Calendar.MINUTE, 0);
    today.set(Calendar.SECOND, 0);
    today.set(Calendar.MILLISECOND, 0);
    
    // Get the date 3 months ago
    Calendar threeMonthsAgo = Calendar.getInstance();
    threeMonthsAgo.add(Calendar.YEAR, -12);
    
    // Get the date 60 years ago
    Calendar sixtyYearsAgo = Calendar.getInstance();
    sixtyYearsAgo.add(Calendar.YEAR, -60);

    // Check if the selected date of birth is within the valid range
    if (dob.before(sixtyYearsAgo.getTime())) {
        // If date is more than 60 years ago
        JOptionPane.showMessageDialog(null, "Age must not exceed 60 years.");
        return false;
    } else if (dob.after(threeMonthsAgo.getTime())) {
        // If date is less than 3 months ago
        JOptionPane.showMessageDialog(null, "Minimum age required is 12 Years.");
        return false;
    }
    
    // If the date is valid (within 3 months to 60 years range), return true
    return true;
}
public boolean isEmailValid(String email) {
    // Regular expression pattern for validating email addresses
    String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";

    // Compile the regular expression into a Pattern object
    Pattern pattern = Pattern.compile(emailRegex);

    // If the email is null or empty, show an error message and return false
    if (email == null || email.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Email cannot be empty. Please enter a valid email address.");
        return false;
    }

    // Match the email against the regex pattern
    Matcher matcher = pattern.matcher(email);
    
    // If the email doesn't match the pattern, show an error message
    if (!matcher.matches()) {
        JOptionPane.showMessageDialog(null, "Invalid email format. Please enter a valid email address.");
        return false;
    }

    // If the email matches the pattern, return true
    return true;
}
