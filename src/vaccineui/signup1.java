public boolean isAadhaarValid(String aadhaarNumber) {
    // Check if the Aadhaar number is null or empty
    if (aadhaarNumber == null || aadhaarNumber.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Aadhaar number cannot be empty. Please enter a valid Aadhaar number.");
        return false;
    }

    // Check if the Aadhaar number contains exactly 12 digits
    if (aadhaarNumber.length() != 12) {
        JOptionPane.showMessageDialog(null, "Invalid Aadhaar number length. Aadhaar number must be 12 digits.");
        return false;
    }

    // Check if the Aadhaar number contains only digits
    if (!aadhaarNumber.matches("\\d{12}")) {
        JOptionPane.showMessageDialog(null, "Invalid Aadhaar number. Aadhaar number must contain only digits.");
        return false;
    }

    // If the Aadhaar number is valid, return true
    return true;
}
